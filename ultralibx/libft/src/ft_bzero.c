#include "../includes/libft.h"

void	ft_bzero(void *s, size_t num)
{
	char	*tmp;
	size_t	i;

	i = 0;
	if (num)
	{
		tmp = s;
		while (i < num)
		{
			tmp[i] = '\0';
			i++;
		}
	}
}
